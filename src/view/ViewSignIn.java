    public boolean validator() {
        if (txtPassword.getText().isBlank() && txtUserName.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Nhập Tài Khoản Và Mật Khẩu Để Tiếp Tục ");
            return false;
        } else if (txtPassword.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Mật Khẩu !");
            return false;

        } else if (txtUserName.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Tài Khoản !");
            return false;
        }
        return true;
    }

