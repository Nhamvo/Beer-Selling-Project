    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        if (validator()) {

            try {
                int check = s.showAccount(txtUserName.getText(), txtPassword.getText());
                if (check == 1) {
                    TrangChu ql = new TrangChu(txtUserName.getText(), "Nhân Viên");
                    JOptionPane.showMessageDialog(this, "Đăng Nhập Thành Công Với Chức Vụ Nhân Viên ", "Bia Nhóm 4", 1);
                    ql.setVisible(true);
                    this.dispose();
                } else if (check == 2) {
                    TrangChu ql = new TrangChu(txtUserName.getText(), "Quản Lý");
                    JOptionPane.showMessageDialog(this, "Đăng Nhập Thành Công Với Chức Vụ Quản Lý", "Bia Nhóm 4", 1);
                    ql.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Tài Khoản Hoặc Mật Khẩu Không Chính Xác");

                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

